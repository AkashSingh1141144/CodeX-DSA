#include <iostream>
using namespace std;

int main() {
	// Boolean Data Type in C++

	bool isCodingFun = true;
	bool isRainyDay = false;
	cout << "Is coding fun? " << (isCodingFun ? "Yes" : "No") << endl;
	
	cout << "Is it a rainy day? " << (isRainyDay ? "Yes" : "No") << endl;

	cout << isCodingFun << endl;
	cout << isRainyDay << endl;
	return 0;
}