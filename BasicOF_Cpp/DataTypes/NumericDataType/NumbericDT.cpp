#include <iostream>
using namespace std;

int main() {
	// Numeric Data Types in C++

	int myInt = 2323; 
	int myInt2 = 4121;

	// Floating-point types 
	float myFloat = 3.14f;
	float myFloat2 = 234.53;

	// Double-precision floating-point types
	double myDouble = 232423.34321;
	double myDouble2 = 234.234234;

	cout << "Integer 1: " << myInt << endl;
	cout << "Integer 2: " << myInt2 << endl;
	cout << "Float 1: " << myFloat << endl;
	cout << "Float 2: " << myFloat2 << endl;
	cout << "Double 1: " << myDouble << endl;
	cout << "Double 2: " << myDouble2 << endl;
	return 0;
}